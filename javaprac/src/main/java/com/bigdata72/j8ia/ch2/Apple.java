package com.bigdata72.j8ia.ch2;

public class Apple {
	private String color;
	private int weight;

	public Apple(String c, int w){
		this.color = c;
		this.weight = w;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getWeight() {
		return weight;
	}

	public void setWeight(int weight) {
		this.weight = weight;
	}
}
